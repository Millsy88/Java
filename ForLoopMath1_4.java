public class ForLoopMath1_4 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.println(" a | a*2 | a*3 ");
        int a = 1;
        for (int i = 0; i < 4; i++){           
            System.out.println(" "+ a + " |  " + a*2 + " " + " |  " + a*3);
            a += 1;
        }
    }
    
}
